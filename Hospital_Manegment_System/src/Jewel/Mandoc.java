    private void getAllRecordShow() {
         dna = txtdna.getText();
        edi = txtedi.getText();
        dpa = txtdpa.getText();
        cfe = txtcfe.getText();
        ex = txtex.getText();
        la = txtla.getText();
        cdi = txtcdi.getText();
        co = txtco.getText();
        Connection con = null;
        Statement stmt = null;
        ResultSet rs = null;
        try {
            con = DBConnection.getDBConnection();
            //Create the Statement object
            stmt = con.createStatement();
            String did = txtedi.getText();
            //Execute the query
            rs = stmt.executeQuery("select dnam, city,depart,exp,ladd,cfees,cont from mdoc where did= '" + did + "' ");
            int count = 0;
            while (rs.next()) {
                //txtedi.setText(rs.getString("did"));
                txtdna.setText(rs.getString("dnam"));
                txtcdi.setText(rs.getString("city"));
                
                txtdpa.setText(rs.getString("depart"));
                txtex.setText(rs.getString("exp"));
                
                txtla.setText(rs.getString("ladd"));
                txtcfe.setText(rs.getString("cfees"));
                txtco.setText(rs.getString("cont"));
                
                
                

                count++;
            }
            if (count == 0) {
                JOptionPane.showMessageDialog(rootPane, "There is no such record");
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                //Close the connection and statement object
                con.close();
                stmt.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }

